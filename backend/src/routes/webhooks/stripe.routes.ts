// Allowlist mode flag (can be disabled for testing)
const ENABLE_IP_ALLOWLIST = process.env.ENABLE_STRIPE_IP_ALLOWLIST !== 'false';

/**
 * Middleware to check if request IP is in Stripe webhook allowlist
 */
export const stripeIPAllowlist = async (
  req: Request,
  res: Response,
  next: NextFunction
): Promise<void> => {
  // Skip IP check if allowlist is disabled (e.g., for testing)
  if (!ENABLE_IP_ALLOWLIST) {
    return next();
  }

  // Get client IP (handle proxies)
  const clientIp = (req.headers['x-forwarded-for'] as string)?.split(',')[0]?.trim()
    || req.ip
    || req.socket.remoteAddress
    || '';

  // Normalize IPv6 addresses
  const normalizedIp = clientIp.replace(/^::ffff:/, '');

  // Check if IP is in allowlist
  if (!STRIPE_WEBHOOK_IPS.has(normalizedIp)) {
    logger.warn('Stripe webhook request from non-allowlisted IP', {
      ip: normalizedIp,
      path: req.path,
      action: 'STRIPE_IP_REJECTED',
      allowlistSize: STRIPE_WEBHOOK_IPS.size
    });

    res.status(403).json({
      error: 'Forbidden',
      message: 'This webhook endpoint is not accessible from your IP address'
    });
    return;
  }

  logger.debug('Stripe webhook IP verified', {
    ip: normalizedIp,
    path: req.path,
    action: 'STRIPE_IP_VERIFIED'
  });

  next();
};

/**
 * Stripe Webhook Handler
 *
 * Uses raw body buffer for Stripe signature verification.
 * Implements the outbox pattern for reliable processing.
 */
router.post(
  '/stripe',
  express.raw({ type: 'application/json' }),
  stripeIPAllowlist,
  maintenanceCheck,
  verifyWebhookSignature,
  asyncHandler(async (req: Request, res: Response) => {
    const signature = req.headers['stripe-signature'] as string;

    if (!signature) {
      logger.warn('Stripe webhook received without signature', {
        action: 'STRIPE_WEBHOOK_NO_SIGNATURE',
      });
      res.status(400).json({ error: 'No signature provided' });
      return;
    }

    let event: Stripe.Event;

    try {
      // Get raw body
      const rawBody = (req as Request & { rawBody?: Buffer }).rawBody || req.body;

      // Initialize Stripe
      const stripe = new Stripe(process.env.STRIPE_SECRET_KEY || '', {
        apiVersion: '2023-10-16',
      });

      // Verify and construct the event using Stripe's built-in method
      event = stripe.webhooks.constructEvent(
        rawBody as Buffer,
        signature,
        process.env.STRIPE_WEBHOOK_SECRET || ''
      );

      logger.info('Stripe webhook signature verified', {
        eventType: event.type,
        eventId: event.id,
        action: 'STRIPE_SIGNATURE_VERIFIED',
      });

    } catch (err: any) {
      logger.error('Stripe webhook signature verification failed', {
        error: err.message,
        signature: signature.substring(0, 20) + '...',
        action: 'STRIPE_SIGNATURE_FAILED',
      });
      res.status(400).json({ error: `Webhook Error: ${err.message}` });
      return;
    }

    // Process the event
    try {
      const result = await handleWebhookEventWithOutbox(event);

      logger.info('Stripe webhook event processed', {
        eventType: event.type,
        eventId: event.id,
        handled: result.handled,
        message: result.message,
        action: 'STRIPE_WEBHOOK_PROCESSED',
      });

      res.json({
        received: true,
        message: result.message,
        eventId: event.id,
      });

    } catch (err: any) {
      logger.error('Stripe webhook event handling failed', {
        eventType: event.type,
        eventId: event.id,
        error: err.message,
        action: 'STRIPE_WEBHOOK_HANDLING_FAILED',
      });

      // Return 500 for processing errors
      res.status(500).json({
        error: `Webhook handling error: ${err.message}`,
        eventId: event.id,
      });
    }
  })
);

